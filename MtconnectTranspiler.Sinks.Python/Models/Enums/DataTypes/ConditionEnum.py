class ConditionEnum(MTCEnum):
	
		Communications = "   indication that the piece of equipment has experienced a communications failure.   "
		
		DatARange = "   indication that the value of the data associated with a measured value or a calculation is outside of an expected range.   "
		
		LogiCProgram = "   indication that an error occurred in the logic program or programmable logic controller (PLC) associated with a piece of equipment.   "
		
		MotioNProgram = "   indication that an error occurred in the motion program associated with a piece of equipment.   "
		
		System = "   general purpose indication associated with an electronic component of a piece of equipment or a controller that represents a fault that is not associated with the operator, program, or hardware.   "
		
		Actuator = "   indication of a fault associated with an actuator.   "
		